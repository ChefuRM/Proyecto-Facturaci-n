    private boolean validar() {
        boolean flag = false;
        if (!"".equals(tTotal.getText())) { //IF INVERSO SI AQUI LA FLAG ES TRUE INGRESA SINO NO
            flag = true;

        } else {
            JOptionPane.showMessageDialog(null, "No has agregado ningun producto :(", "MENSAJE", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!"".equals(tPago.getText())) {
            flag = true;

        } else {
            JOptionPane.showMessageDialog(null, "No se ha realizado ningun tipo de pago :(", "MENSAJE", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!lCCambio.getText().equals("00.00")) {
            flag = true;
            if (Double.parseDouble(lCCambio.getText()) < 0) {
                JOptionPane.showMessageDialog(null, "Error no ha pagado lo suficiente :((", "MENSAJE", JOptionPane.ERROR_MESSAGE);
                return false;

            } else {
                flag = true;

            }
        } else {
            JOptionPane.showMessageDialog(null, "No se ha ingresado bien el metodo de pago :(", "MENSAJE", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!(EmpleadoSel == null)) {
            flag = true;
        } else {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado el empleado a cargo de la caja:(", "MENSAJE", JOptionPane.ERROR_MESSAGE);
            return false;

        }
        if (!(ClienteSel == null)) {
            flag = true;
        } else {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado el cliente:(", "MENSAJE", JOptionPane.ERROR_MESSAGE);
            return false;

        }

        return flag;
    }

    private void correlativo() {

        if (ArregloFac.size() > 0) {
            for (cFactura v : ArregloFac) {
                if (ArregloFac.indexOf(v) == ArregloFac.size() - 1) {
                    Integer correlativo = 1 + Integer.parseInt(v.getCorrelativo());
                    lCorrelativo.setText(correlativo.toString());
                }
            }
        } else {
            lCorrelativo.setText("1");
        }
    }

    public static void leerbinario() {
        FileInputStream fis = null;
        ObjectInputStream ois = null;

        try {

            fis = new FileInputStream("bFacturas.dat");
            ois = new ObjectInputStream(fis);
            ArregloFac = (ArrayList<cFactura>) ois.readObject(); //es necesario el casting

        } catch (FileNotFoundException e) {
            System.out.println(e.getMessage());
        } catch (ClassNotFoundException e) {
            System.out.println(e.getMessage());
        } catch (IOException e) {
            System.out.println(e.getMessage());
        } finally {
            try {
                if (fis != null) {
                    fis.close();
                }
                if (ois != null) {
                    ois.close();
                }
            } catch (IOException e) {
                System.out.println(e.getMessage());
            }
        }
    }

    private void crearbinario() {
        FileOutputStream fos = null;
        ObjectOutputStream ous = null;

        try {
            //Se crea el fichero
            fos = new FileOutputStream("bFacturas.dat");
            ous = new ObjectOutputStream(fos);

            //Se escribe el objeto en el fichero
            ous.writeObject(ArregloFac);

        } catch (FileNotFoundException e) {
            System.out.println("1" + e.getMessage());
        } catch (IOException e) {
            System.out.println("2" + e.getMessage());
        } finally {
            try {
                if (fos != null) {
                    fos.close();
                }
                if (ous != null) {
                    ous.close();
                }
            } catch (IOException e) {
                System.out.println("3" + e.getMessage());
            }
        }
    }

